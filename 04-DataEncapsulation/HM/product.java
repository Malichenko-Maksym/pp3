class Product{
    private String name;
    private boolean isVegetarian;

    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return name;
    }

    public void setIsVegetarian(boolean isVegetarian){
        this.isVegetarian = isVegetarian;
    }

    public boolean getIsVegetarian(){
        return isVegetarian;
    }
}